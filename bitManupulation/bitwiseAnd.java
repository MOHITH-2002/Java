package bitManupulation;

public class bitwiseAnd {
    public static void main(String[] args) {
        System.out.print("Bitwise of and is:");
        System.out.println(5 & 6); // on;y single &
        System.out.println(5 | 6); // on;y single |
        System.out.println(~-6); // on;y single ~ not gate
        System.out.println(5 ^ 6); // on;y single ^ xor

    }
}
